#   x*2**3 + x*4**9 = 3877 + 40**2*5
#   x*(2**3 + 4**9) = 3877 + 40**2*5
#   x = (3877 + 40**2*5)/(2**3 + 4**9)
ans = str((3877 + 40**2*5)/(2**3 + 4**9))
for i in range(0,len(ans)):
    if ans[i] == "." :
        i = i + 1 # так как числа после запятой начинаются со следующего знака
        break
print(ans)
